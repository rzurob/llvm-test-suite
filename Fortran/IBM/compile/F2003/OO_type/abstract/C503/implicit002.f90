module m
   
   type, abstract :: base
      integer :: id
   contains
      procedure, nopass :: print
   end type
   
   type, extends(base) :: child
   end type
   
contains
   subroutine print()
      print *,'hello'
   end subroutine
end module

program implicit002
   use m  
   IMPLICIT type(base) (A-F)
   
   call Aa%print()
   call Bb%print()
   
   Aa%id = 5
   Bb%id = 5
      
end program