  MODULE M

    TYPE  :: Zero(K1,N1)    ! (4,20)
        INTEGER, KIND :: K1
        INTEGER, LEN  :: N1
      private
    END TYPE

    TYPE, EXTENDS(Zero)  :: Base    ! (4,20)
      INTEGER(K1) :: BaseId = 1
    CONTAINS
      PROCEDURE, PASS   :: GetId => GetBaseId
      PROCEDURE, NoPASS   :: SetId => SetBaseId
    END TYPE

    TYPE, EXTENDS(Base) :: Child    ! (4,20)
      INTEGER(K1)  :: ChildId = 2
    CONTAINS
      PROCEDURE, PASS   :: GetId => GetChildId
      PROCEDURE, NoPASS   :: SetId => SetChildId
    END TYPE

    CONTAINS

    ELEMENTAL FUNCTION GetChildId(Arg)
    CLASS(Child(4,*)), INTENT(IN) :: Arg
    INTEGER                  :: GetChildId
      GetChildId = Arg%ChildId
    END FUNCTION

    ELEMENTAL FUNCTION GetBaseId(Arg)
    CLASS(Base(4,*)), INTENT(IN)  :: Arg
    INTEGER                  :: GetBaseId
      GetBaseId = Arg%BaseId
    END FUNCTION

    SUBROUTINE SetBaseId(Arg)
    CLASS(Base(4,*)) :: Arg(:)
      Arg%BaseId =  -Arg%BaseId
    END SUBROUTINE

    SUBROUTINE SetChildId(Arg)
    CLASS(Base(4,*))  :: Arg(:)
      SELECT TYPE(Arg)
        TYPE IS (Child(4,*))
          Arg%ChildId = -Arg%ChildId
      END SELECT
    END SUBROUTINE


  END MODULE


  PROGRAM AttrTargetArrVec
  USE M
  TYPE (Child(4,20)),   TARGET :: W(3:8)=Child(4,20)(BaseID=-1, ChildID=-2)
  CLASS (Zero(4,:)),  POINTER :: PtrZ(:)
  CLASS (Base(4,:)),  POINTER :: PtrB(:)
  CLASS (Child(4,:)), POINTER :: PtrC(:)
  INTEGER                :: S1(1)=(/4/), S2(2)=(/6,8/)

  ! all ptr assingments should fail!

  ASSOCIATE ( As => W((/S1,S2/)) )

    PtrZ => As%Base%Zero
    PtrB => As%Base
    PtrC => As

    ASSOCIATE ( As => As(:)%Base )

      PtrZ => As%Zero
      PtrB => As

    END ASSOCIATE

  END ASSOCIATE

  END
