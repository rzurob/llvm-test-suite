module m
    type base
        integer*4 :: id

        contains

        procedure :: print => printBase
    end type

    type, extends(base) :: child
        character*20 :: name

        contains

        procedure :: print => printChild
    end type

    contains

    subroutine printBase (b)
        class (base), intent(in) :: b

        print *, b%id
    end subroutine

    subroutine printChild (b)
        class (child), intent(in) :: b

        print *, b%id, b%name
    end subroutine
end module

program fpAssgn030d
use m
    class (base), pointer :: b_ptr

    type (child) :: c1 = child (1, 'c1')

    associate (x => c1)
        b_ptr => x      !<-- this is illegal

        call b_ptr%print
    end associate

end
