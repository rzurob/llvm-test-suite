module m
    type base
        integer*4 :: id

        contains

        procedure :: replicate => produceBase

        final :: finalizeBase
    end type

    contains

    subroutine finalizeBase (b)
        type (base), intent(inout) :: b

        print *, 'finalizeBase'

        b%id = 0
    end subroutine

    type (base) function produceBase (b)
        class (base), intent(in) :: b

        produceBase%id = b%id
    end function
end module

