module m
   type base (kb)
      integer, kind :: kb
      sequence
      integer(kb) :: i
   end type

   interface write(formatted)
      subroutine writeformatted(dtv, unit, iotype, v_list, iostat, iomsg )
         import base
         type(base(4)), intent(in) :: dtv ! tcx: (4)
         integer,  intent(in) :: unit
         character(*), intent(in) :: iotype
         integer, intent(in)     :: v_list(:)
         integer,  intent(out) :: iostat
         character(*),  intent(inout) :: iomsg
      end subroutine
   end interface

end module

program misc003kl
   use m

   integer :: stat
   character(200) :: msg = ''
   type(base(4))               :: b1 ! tcx: (4)
   type(base(4)), pointer      :: b2 ! tcx: (4)
   type(base(4)), allocatable  :: b3 ! tcx: (4)

   namelist /nml/  b1, b2
   namelist /nml1/ b1, b2, b3

   open (1, file = 'misc003kl.1', form='formatted', access='sequential' )
   allocate(b2, b3)

   b1%i = 2
   b2%i = 4
   b3%i = 6

   write (1,NML=nml, iostat=stat, iomsg=msg)
   if (( stat /=  0 ) .or. ( msg /= 'dtiowrite' ) ) error stop 1_4

   write (1,NML=nml1, iostat=stat, iomsg=msg)
   if (( stat /=  0 ) .or. ( msg /= 'dtiowrite' ) ) error stop 2_4

end program


subroutine writeformatted (dtv, unit, iotype, v_list, iostat, iomsg)
   use m, only: base

   type(base(4)), intent(in) :: dtv ! tcx: (4)
   integer, intent(in) :: unit
   character(*), intent(in) :: iotype
   integer, intent(in)     :: v_list(:)
   integer, intent(out) :: iostat
   character(*), intent(inout) :: iomsg

   namelist /dtio/ dtv
 
   if ( iotype /= "NAMELIST" ) error stop 3_4
   if ( size(v_list, 1) /= 0 ) error stop 4_4

   write (unit, dtio , iostat=iostat )

   iomsg = 'dtiowrite'

end subroutine


