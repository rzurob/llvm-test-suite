module m
    type base(k1)    ! (4)
        integer, kind :: k1
        integer(k1)   :: id = 0

        contains

        procedure :: print => printBase
    end type

    type, extends(base) :: child(k2,n1)    ! (4,1,20)
        integer, kind             :: k2
        integer, len              :: n1
        character(kind=k2,len=n1) :: name = ''

        contains

        procedure :: print => printChild
    end type

    type (child(4,1,20)), parameter :: c1_m = child(4,1,20) (1, name = 'c1_m')
    contains

    subroutine printBase (b)
        class (base(4)), intent(in) :: b

        print *, b%id
    end subroutine

    subroutine printChild (b)
        class (child(4,1,*)), intent(in) :: b

        print *, b%id, b%name
    end subroutine

    subroutine printData (d)
        class (base(4)), intent(in) :: d

        call d%print
    end subroutine
end module

program fArg502a
use m, only : child, printData, c1_m
    type (child(4,1,20)), parameter :: c1 = child(4,1,20) (id = 10, name = 'c1')

    call printData (c1_m)

    call printData (c1_m%base)

    call printData (c1)

    call printData (c1%base)
end
