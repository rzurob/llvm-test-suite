module m
    type base(k1)    ! (4)
        integer, kind :: k1
        integer(k1)   :: id

        contains

        procedure :: print => printBase
    end type

    type, extends(base) :: child(k2,n1)    ! (4,1,20)
        integer, kind             :: k2
        integer, len              :: n1
        character(kind=k2,len=n1) :: name

        contains

        procedure :: print => printChild
    end type

    contains

    subroutine printBase (b)
        class (base(4)), intent (in) :: b

        print *, b%id
    end subroutine

    subroutine printChild (b)
        class (child(4,1,*)), intent(in) :: b

        print *, b%id, b%name
    end subroutine

    subroutine printBaseArray (b)
        class (base(4)), intent(in) :: b(:)

        do i = 1, size(b)
            call b(i)%print
        end do
    end subroutine
end module

program fArg523
use m
    class (base(4)), allocatable :: b1
    class (base(4)), pointer :: b2(:)

    type (child(4,1,20)), target :: c1(10)

    c1%id = (/(i, i=1, 10)/)
    c1%name = 'xlftest team'

    b2 => c1(::2)

    allocate (b1, source=child(4,1,20)(100, 'b2 scalar'))

    call printBaseArray ((/b1, b2/))
end
