program falloc026
    real(8), pointer :: d1(:) => null()
    real(8), target :: d2(10)

    integer error

    deallocate (d1, stat=error)

    if (error == 0) error stop 1_4

    allocate (d1(10))

    error = 0

    deallocate (d1)
    deallocate (d1, stat=error)

    if (error == 0) error stop 2_4

    d1 => d2

    nullify (d1)

    deallocate (d1, stat=error)

    if (error == 0) error stop 3_4
end
