module m
    type base
        integer*4 :: id

        contains

        final :: finalizeBase
    end type

    type, extends (base) :: child
        character*20 :: name

        contains

        final :: finalizeChild
    end type

    type container
        class (base), allocatable :: data
    end type

    contains

    subroutine finalizeBase (b)
        type (base), intent(in) :: b

        print *, 'finalizeBase'
    end subroutine

    subroutine finalizeChild (c)
        type (child), intent(in) :: c

        print *, 'finalizeChild'
    end subroutine
end module

