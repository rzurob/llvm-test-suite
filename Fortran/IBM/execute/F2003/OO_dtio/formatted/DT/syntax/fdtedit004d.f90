module m
   type base
      integer(4) :: i
   end type

   interface read(formatted)
      subroutine readformatted(dtv, unit, iotype, v_list, iostat, iomsg )
         import base
         class(base), intent(inout) :: dtv
         integer,  intent(in) :: unit
         character(*), intent(in) :: iotype
         integer, intent(in)     :: v_list(:)
         integer,  intent(out) :: iostat
         character(*),  intent(inout) :: iomsg
      end subroutine
   end interface

   interface write(formatted)
      subroutine writeformatted(dtv, unit, iotype, v_list, iostat, iomsg )
         import base
         class(base), intent(in) :: dtv
         integer,  intent(in) :: unit
         character(*), intent(in) :: iotype
         integer, intent(in)     :: v_list(:)
         integer,  intent(out) :: iostat
         character(*),  intent(inout) :: iomsg
      end subroutine
   end interface

end module

program fdtedit004d
use m

   class(base), allocatable :: b1
   type(base)               :: b2  = base(-999)

   integer :: stat
   character(200) :: msg = ''
   character(10)  :: format

   format="(DT())"

   open (1, file = 'fdtedit004d.1', form='formatted', access='sequential' )

   allocate ( b1, source = base(100) )

   write ( 1, fmt=format ) b1   !<- runtime will issue a runtime msg, and runtime will do DT instaed of DT()
                                !   DTIO is still invoked
   rewind 1

   read ( 1, format      ) b2
   if ( b2%i /= 100 )  error stop 2_4

end program


subroutine readformatted (dtv, unit, iotype, v_list, iostat, iomsg)
use m, only: base

   class(base), intent(inout) :: dtv
   integer, intent(in) :: unit
   character(*), intent(in) :: iotype
   integer, intent(in)     :: v_list(:)
   integer, intent(out) :: iostat
   character(*), intent(inout) :: iomsg

   if ( iotype /= "DT" ) error stop 3_4
   if ( size(v_list, 1) /= 0 ) error stop 4_4

   read ( unit, "(I4)" ) dtv%i

end subroutine

subroutine writeformatted (dtv, unit, iotype, v_list, iostat, iomsg)
use m, only: base

   class(base), intent(in) :: dtv
   integer, intent(in) :: unit
   character(*), intent(in) :: iotype
   integer, intent(in)     :: v_list(:)
   integer, intent(out) :: iostat
   character(*), intent(inout) :: iomsg

   if ( iotype /= "DT" ) error stop 5_4
   if ( size(v_list, 1) /= 0 ) error stop 6_4

   write ( unit, * ) dtv%i

end subroutine
